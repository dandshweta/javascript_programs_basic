function findPalindrome(str) {
  str.toLowerCase().split(" ");
  for (let i = 0; i <= 0; i++) {
    let word = str[i];
    let newstr = [];
    for (let j = word.length; j >= 0; j--) {
      newstr.word[j];
    }
    if (str[i] === newstr) {
      return "word is palindrome";
    } else {
      return "word is not palindrome";
    }
  }
}

